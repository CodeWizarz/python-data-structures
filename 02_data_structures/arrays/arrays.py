# arrays.py

# Creating an array
arr = [1, 2, 3, 4, 5]

# Accessing elements
print(arr[0])  # Output: 1

# Modifying elements
arr[1] = 20
print(arr)  # Output: [1, 20, 3, 4, 5]

# Adding elements
arr.append(6)
print(arr)  # Output: [1, 20, 3, 4, 5, 6]

# Removing elements
arr.pop(2)
print(arr)  # Output: [1, 20, 4, 5, 6]
